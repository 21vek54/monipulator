// Авто-восстановление после выхода из ALM (через 1 секунду)
#define AUTO_RECOVER_AFTER_ALM        1
#define AUTO_RECOVER_AFTER_ALM_DELAY_MS 1000

// Разрешить автоподхват в loop (если EEPROM содержит шаг и система свободна)
#define AUTO_RECOVER_IN_LOOP          1

// Защита от зацикливания
#define AUTO_RECOVER_MAX_ATTEMPTS     3
#define AUTO_RECOVER_COOLDOWN_MS      15000UL
