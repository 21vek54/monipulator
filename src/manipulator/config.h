// ===== УПРАВЛЕНИЕ ШАГОВЫМ ДВИГАТЕЛЕМ (драйвер DM556) =====
#define STEP_PIN          18    // Пульсовый вход STEP драйвера (каждый импульс = 1 шаг)
#define DIR_PIN           5     // Направление вращения DIR (HIGH = вправо, LOW = влево)
#define ENABLE_PIN        19    // Включение драйвера ENABLE (LOW = драйвер включен)
#define ALM_PIN           23    // Аварийный сигнал драйвера ALM (LOW = авария, HIGH = норма)

// ===== РЕЛЕ ДЛЯ АВАРИЙНОГО СБРОСА ДРАЙВЕРА =====
#define RELAY_PIN         15    // Реле для перезагрузки драйвера (HIGH = питание отключено)
#define RELAY_RESET_TIME  2000  // Время сброса драйвера (миллисекунды)

// ===== ГЕРКОНЫ ГОРИЗОНТАЛЬНОГО ПЕРЕМЕЩЕНИЯ =====
// Нормально-разомкнутые (NO), замыкаются на GND при нажатии
// Используются для определения крайних положений каретки
#define LIMIT_SWITCH_LEFT  16   // Левый концевой датчик (пин подключения)
#define LIMIT_SWITCH_RIGHT 17   // Правый концевой датчик (пин подключения)

// ===== ПНЕВМАТИКА - УПРАВЛЕНИЕ МОСФЕТАМИ (выходы) =====
// MOSFET управляют соленоидными клапанами (12В)
#define GRIP_SOL_PIN       25   // Захват: HIGH = открыть, LOW = закрыть
#define Z_SOL_PIN          26   // Вертикаль: HIGH = опустить, LOW = поднять

// ===== ПНЕВМАТИКА - ГЕРКОНЫ ПОЛОЖЕНИЯ (входы) =====
// Нормально-разомкнутые (NO), замыкаются на GND в крайних положениях
// Датчики положения пневмоцилиндров
#define GRIP_OPEN_SW       32   // LOW = захват полностью открыт
#define GRIP_CLOSED_SW     33   // LOW = захват полностью закрыт
#define Z_UP_SW            27   // LOW = вертикаль в верхнем положении
#define Z_DOWN_SW          14   // LOW = вертикаль в нижнем положении

// ===== ГЕОМЕТРИЧЕСКИЕ ПАРАМЕТРЫ (заводские настройки) =====
#define DEFAULT_STEPS      29053     // Полное расстояние между концевиками в шагах (рассчитано)
#define DEFAULT_MM         724.0     // Полное расстояние между концевиками в миллиметрах (измерено)
#define DEFAULT_STEPS_PER_MM 40.128  // Коэффициент перевода: шагов на миллиметр (29053 / 724)

// ===== НАСТРОЙКИ ФИЛЬТРАЦИИ ДРЕБЕЗГА ДЛЯ ВСЕХ ГЕРКОНОВ =====
#define DEBOUNCE_SAMPLES   5     // Количество последовательных одинаковых показаний для смены состояния
#define DEBOUNCE_INTERVAL  2     // Интервал между опросами датчика (миллисекунды)

// ===== НАСТРОЙКИ S-ПРОФИЛЯ ДЛЯ УСКОРЕННОЙ КАЛИБРОВКИ =====
// Профиль: РАЗГОН - ПОЛКА (макс. скорость) - ТОРМОЖЕНИЕ
#define S_PROFILE_PLATEAU_STEPS  17700  // Длина участка постоянной скорости (шагов)
#define S_PROFILE_START_DELAY    1000   // Начальная задержка (1000 мкс = 1000 шаг/сек)
#define S_PROFILE_MAX_DELAY      1000   // Максимальная задержка (медленная скорость, 1000 шаг/сек)
#define S_PROFILE_MIN_DELAY      200    // Минимальная задержка (5000 шаг/сек = 200 мкс)
#define S_PROFILE_ACCEL_STEPS    1000   // Количество шагов на фазе разгона
#define S_PROFILE_DECEL_STEPS    1000   // Количество шагов на фазе торможения
#define CALIBRATION_SAFETY_DISTANCE 1000 // Отъезд от концевика перед началом измерения (шагов)

// ===== ПАРАМЕТРЫ БЕЗОПАСНОСТИ И ОГРАНИЧЕНИЯ =====
#define CALIBRATION_MAX_STEPS 1000000    // Максимальное количество шагов при калибровке (защита от бесконечного цикла)
#define STEP_CHECK_INTERVAL   10         // Проверять концевик каждые N шагов во время движения

// ===== НАСТРОЙКИ ТЕСТА ОТКАЗОУСТОЙЧИВОСТИ =====
#define TEST_DELAY_BETWEEN_MOVES 2000    // Пауза между циклами теста (миллисекунды)
#define MAX_TEST_CYCLES          1000    // Максимальное количество циклов теста

// ===== ПАРАМЕТРЫ ПНЕВМАТИКИ =====
#define PNEUMO_TIMEOUT_MS       3000     // Таймаут ожидания срабатывания пневматики (мс)
#define PNEUMO_POST_DELAY_MS    20       // Пауза после срабатывания датчика пневматики (мс)
#define PNEUMO_POLL_INTERVAL_MS 10       // Интервал опроса датчиков пневматики (мс)

// ===== ПАРАМЕТРЫ ДВИЖЕНИЯ =====
#define MAX_PROFILE_SEGMENTS    50       // Максимальное количество сегментов в профиле скорости

// ===== НАСТРОЙКИ EEPROM =====
#define EEPROM_SIZE            512       // Размер EEPROM (байт)
#define EEPROM_START_ADDR      0         // Начальный адрес
#define EEPROM_MAGIC_NUMBER    0x55AA    // Магическое число для проверки
#define EEPROM_VERSION         2         // Версия структуры данных (2 - с калибровкой)

// ===== СТРУКТУРА ДАННЫХ EEPROM =====
typedef struct {
    uint16_t magic;             // Магическое число (0x55AA)
    uint8_t version;            // Версия структуры (2)
    uint8_t workCycleStep;      // Текущий шаг рабочего цикла (0-8)
    uint32_t savedTime;         // Время сохранения (секунды с момента запуска)
    uint8_t systemState;        // Состояние системы (битовые флаги)
    bool isCalibrated;          // Флаг калибровки (true/false)
    uint32_t travelDistance;    // Расстояние между концевиками в шагах
    uint8_t reserved[52];       // Резерв для будущих расширений
} EEPROMData;

